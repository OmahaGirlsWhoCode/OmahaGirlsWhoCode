$(document).ready(function() {
  function getRandomSize(min, max) {
    return Math.round(Math.random() * (max - min) + min);
  }
  for (var i = 0; i < pics.length; i++) {
    var width = getRandomSize(200, 400);
    var height =  getRandomSize(200, 400);
    $('#photos').append('<div class="img-container" style="width='+ width + 'px; height=' + height + 'px">' +
      '<img src="' + pics[i].source + '">' +
      '<input type="checkbox" id="toggle-' + pics[i].id + '">' +
      '<label for="toggle-' + pics[i].id + '">' +
        '<div class="glass" style="height: 100%; line-height=' + height + 'px">' +
          '<span>' + pics[i].caption + '</span></div>' +
        '</div>' +
      '</label>');
  }
});